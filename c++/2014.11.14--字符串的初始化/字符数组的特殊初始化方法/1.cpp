#include <iostream>

void main()
{
	char s1[5] = {1,2,'3',4,'5'};
	char s2[5] = {1,2,'3'};
	char s3[] = {1,2,'3',4};

	//字符数组的三种特殊的初始化方式
	//01）右侧需要5个字节才能存储，所以s4的
	//长度需要为5
	char s4[5] = "abcd";

	//右侧需要5个字节才能存储，s5的长度可以大于5
	char s5[7] = "efgh";

	//右侧需要5个字节才能存储，s6的长度将被编译器自动设置
	//5
	char s6[] = "ijkl";
	system("pause");
}