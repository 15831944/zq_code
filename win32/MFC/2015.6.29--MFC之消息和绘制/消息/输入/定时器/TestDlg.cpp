void CTestDlg::OnTimer(UINT_PTR nIDEvent)
{
	//nIDEvent就是定时器的id号

	if (0 == nIDEvent)
	{
		seconds += 1;
		char buf[32];
		sprintf_s(buf, 32, "%d秒", seconds);
		SetWindowTextA(buf);
	}
	else if (1 == nIDEvent)
		Beep(500, 100);

	CDialog::OnTimer(nIDEvent);
}
